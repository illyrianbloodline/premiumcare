def get_t(lang: str) -> dict:
    """
    Return translations for the requested language.

    Fallback behavior:
    - Start with English
    - Override with requested language values if available
    - Prevent KeyError when a translation key is missing in another language
    """
    base = deepcopy(TRANSLATIONS["en"])
    selected = TRANSLATIONS.get(lang, {})
    base.update(selected)
    return base


def validate_translations() -> dict:
    """
    Returns missing and extra keys by language.
    Useful during development.
    """
    base_keys = set(TRANSLATIONS["en"].keys())
    report = {}

    for lang_code, values in TRANSLATIONS.items():
        keys = set(values.keys())
        report[lang_code] = {
            "missing": sorted(base_keys - keys),
            "extra": sorted(keys - base_keys),
        }

    return report
